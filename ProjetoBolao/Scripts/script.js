$('#modalNome').on('http://show.bs .modal', function (event) {
    var button = $(event.relatedTarget)
    var recipient = "http://button.data('whatever')";
    recipient = recipient.link(recipient);
    var modal = $(this);
    const article = document.querySelector('#modalNome');
    article.dataset.columns // "3"
    article.dataset.indexNumber // "12314"
    article.dataset.parent // "cars"
    modal.find('.modal-title').text('Alterar ' + recipient)
    modal.find('.modal-body input').val("")
})